export type ContratoTirzepatidaPlaceholderKey = (typeof CONTRATO_TIRZEPATIDA_PLACEHOLDER_KEYS)[number];

const PLACEHOLDER_REGEX = /\{\{(\w+)\}\}/g;

function normalizarValorContrato(value: unknown, key?: ContratoTirzepatidaPlaceholderKey): string {
  if (value == null) {
    if (key === 'opcao1' || key === 'opcao2') return '';
    return 'Não informado';
  }

  const normalized = String(value).trim();

  if (!normalized) {
    if (key === 'opcao1' || key === 'opcao2') return '';
    return 'Não informado';
  }

  return normalized;
}

/**
 * Substitui placeholders {{chave}} pelos valores informados.
 *
 * Chaves ausentes viram "Não informado", exceto:
 * - assinaturaDigitalMedico / assinaturaDigitalPaciente: legado (leitura)
 * - opcao1 / opcao2: "X" ou vazio (marcação de opção do paciente)
 * - dataAssinaturaMedico / dataAssinaturaPaciente: "—"
 * - cidadeForo/estadoForo: caem para cidadeMedico/estadoMedico quando possível
 */
export function fillContratoTirzepatidaTemplate(
  values: Partial<Record<ContratoTirzepatidaPlaceholderKey, string>>,
  templateText: string = CONTRATO_TIRZEPATIDA_TEMPLATE
): string {
  const resolvedValues: Partial<Record<ContratoTirzepatidaPlaceholderKey, string>> = {
    ...values,
    cidadeForo: values.cidadeForo || values.cidadeMedico,
    estadoForo: values.estadoForo || values.estadoMedico,
    assinaturaDigitalMedico:
      values.assinaturaDigitalMedico && values.assinaturaDigitalMedico.trim()
        ? values.assinaturaDigitalMedico
        : 'Pendente de assinatura digital do(a) médico(a).',
    assinaturaDigitalPaciente:
      values.assinaturaDigitalPaciente && values.assinaturaDigitalPaciente.trim()
        ? values.assinaturaDigitalPaciente
        : 'Pendente de assinatura digital do paciente.',
    dataAssinaturaMedico:
      values.dataAssinaturaMedico && values.dataAssinaturaMedico.trim()
        ? values.dataAssinaturaMedico
        : '—',
    dataAssinaturaPaciente:
      values.dataAssinaturaPaciente && values.dataAssinaturaPaciente.trim()
        ? values.dataAssinaturaPaciente
        : '—',
  };

  return templateText.replace(PLACEHOLDER_REGEX, (_match, key: string) => {
    return normalizarValorContrato(
      resolvedValues[key as ContratoTirzepatidaPlaceholderKey],
      key as ContratoTirzepatidaPlaceholderKey
    );
  });
}
